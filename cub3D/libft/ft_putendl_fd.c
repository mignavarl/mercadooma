#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	l;

	l = 0;
	while (s[l] != '\0')
	{
		write (fd, &s[l], 1);
		l++;
	}
	write (fd, "\n", 1);
}
